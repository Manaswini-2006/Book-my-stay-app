/**
 * BookMyStay App - Hotel Booking Management System
 *
 * <p>This class represents the entry point of the BookMyStay application.
 * It demonstrates how a Java program begins execution using the main() method
 * and prints a welcome message to the console.</p>
 *
 * <p>Use Case 1: Application Entry & Welcome Message</p>
 *
 * @author Developer
 * @version 1.0
 */
public class BookMyStayApp {

    /**
     * Main method - Entry point of the Java application.
     * The JVM invokes this method to start the program.
     *
     * @param args Command-line arguments
     */
    public static void main(String[] args) {

        // Display welcome message
        System.out.println("=======================================");
        System.out.println("        Welcome to BookMyStay App      ");
        System.out.println("      Hotel Booking Management System  ");
        System.out.println("            Version: 1.0               ");
        System.out.println("=======================================");

        // Inform the user that the application has started
        System.out.println("Application started successfully.");

        // Application ends after printing the message
    }
}